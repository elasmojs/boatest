try{
    let a = 5;        // 00000000000000000000000000000101
    let b = 3;        // 00000000000000000000000000000011

    console.log(a & b); // 00000000000000000000000000000001
    // expected output: 1

    a = 5;      // 00000000000000000000000000000101
    a &= 3;         // 00000000000000000000000000000011

    console.log(a); // 00000000000000000000000000000001
    // expected output: 1

    a = 5;     // 0000000000000101
    b = -3;    // 1111111111111101

    console.log(~a); // 1111111111111010
    // expected output: -6

    console.log(~b); // 0000000000000010
    // expected output: 2

    
    a = 5;        // 00000000000000000000000000000101
    b = 3;        // 00000000000000000000000000000011

    console.log(a | b); // 00000000000000000000000000000111
    // expected output: 7

    a = 5;      // 00000000000000000000000000000101
    a |= 3;         // 00000000000000000000000000000011

    console.log(a); // 00000000000000000000000000000111
    // expected output: 7

    a = 5;        // 00000000000000000000000000000101
    b = 3;        // 00000000000000000000000000000011

    console.log(a ^ b); // 00000000000000000000000000000110
    // expected output: 6

    a = 5;      // 00000000000000000000000000000101
    a ^= 3;         // 00000000000000000000000000000011

    console.log(a); // 00000000000000000000000000000110
    // expected output: 6
    
    true;
}catch(e){
    console.log(e);
    false;
}